# Number data types

a = 5 # integer
x = -5 # integer
y = 5296465844 # integer
print(a, x, y)
print(type(a))

b = 3.14 # float
d = 3.0 # float
e = -3.0 # float
print(b, d, e)
print(b)
print(type(b))

c = 2 + 3j # complex
print(c)
print(type(c))

# Convert types
# int()
# float()
# complex()

# Convert float to int
f = 3.14
print(f)
print(int(f))

# Convert int to float
g = 5
print(g)
print(float(g))

# Convert int to complex
h = 5
print(h)
print(complex(h))

